import { createClient } from "https://esm.sh/@supabase/supabase-js@2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

Deno.serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const authHeader = req.headers.get("authorization") ?? "";
    const supabaseUrl = Deno.env.get("SUPABASE_URL")!;
    const serviceKey = Deno.env.get("SUPABASE_SERVICE_ROLE_KEY")!;

    const admin = createClient(supabaseUrl, serviceKey);

    const deleted: string[] = [];
    const errors: string[] = [];

    // Delete from 'curriculum' (singular) bucket
    const filesToDelete = ["tongue-twisters.json"];
    const { error: delErr } = await admin.storage
      .from("curriculum")
      .remove(filesToDelete);

    if (delErr) {
      errors.push(`curriculum bucket: ${delErr.message}`);
    } else {
      deleted.push("curriculum/tongue-twisters.json");
    }

    return new Response(
      JSON.stringify({ deleted, errors, message: "Cleanup complete" }),
      {
        status: 200,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  } catch (e) {
    return new Response(JSON.stringify({ error: String(e) }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
